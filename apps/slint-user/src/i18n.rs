pub fn normalize_locale(value: &str) -> String {
    let trimmed = value.trim();
    if SUPPORTED_LOCALES.contains(&trimmed) {
        trimmed.to_string()
    } else {
        DEFAULT_LOCALE.to_string()
    }
}

pub fn translate(locale: &str, key: &str) -> String {
    let locale = normalize_locale(locale);
    text_for(&locale, key)
        .or_else(|| text_for(DEFAULT_LOCALE, key))
        .unwrap_or(key)
        .to_string()
}

fn text_for(locale: &str, key: &str) -> Option<&'static str> {
    match locale {
        "zh-Hans" => zh_hans(key).or_else(|| en(key)),
        "zh-Hant" => zh_hant(key).or_else(|| zh_hans(key)).or_else(|| en(key)),
        "fr" => fr(key).or_else(|| en(key)),
        "de" => de(key).or_else(|| en(key)),
        _ => en(key),
    }
}

