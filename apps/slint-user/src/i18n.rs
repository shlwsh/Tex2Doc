#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn invalid_locale_falls_back_to_english() {
        assert_eq!(normalize_locale("xx"), "en");
        assert_eq!(translate("xx", "tab.convert"), "Convert");
    }

    #[test]
    fn all_supported_locales_resolve_every_ui_key() {
        for locale in SUPPORTED_LOCALES {
            for key in KEYS {
                assert_ne!(translate(locale, key), key, "{locale} missing {key}");
            }
        }
    }
}
