export type Locale = keyof typeof messages;
export type Messages = typeof messages[Locale];
export type CopyKey = keyof typeof zhCopy;
export type FieldKey = keyof typeof fields;
export type StatusKey = keyof typeof statuses;

export function copyText(locale: Locale, key: CopyKey): string {
  return messages[locale].copy[key];
}

export function fieldLabel(locale: Locale, key: string): string | undefined {
  return messages[locale].fields[key as FieldKey];
}

export function statusLabel(locale: Locale, value: string): string | undefined {
  return messages[locale].statuses[value.toLowerCase() as StatusKey];
}
