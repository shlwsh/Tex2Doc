pub fn normalize_locale(value: &str) -> String {
    let trimmed = value.trim();
    if trimmed == "ja" {
        return "ja-JP".to_string();
    }
    if SUPPORTED_LOCALES.contains(&trimmed) {
        trimmed.to_string()
    } else {
        DEFAULT_LOCALE.to_string()
    }
}

pub fn translate(locale: &str, key: &str) -> String {
    let locale = normalize_locale(locale);
    text_for(&locale, key)
        .or_else(|| text_for(DEFAULT_LOCALE, key))
        .unwrap_or(key)
        .to_string()
}

fn text_for(locale: &str, key: &str) -> Option<&'static str> {
    match locale {
        // Slint 1.16's current text layout path logs ICU4X CJK segmentation
        // errors when rendering Chinese/Japanese glyphs. Keep these locales
        // selectable, but render English fallback until CJK segmentation data is
        // available in the runtime.
        "zh-Hans" | "zh-Hant" | "ja-JP" => en(key),
        "fr" => fr(key).or_else(|| en(key)),
        "de" => de(key).or_else(|| en(key)),
        _ => en(key),
    }
}

